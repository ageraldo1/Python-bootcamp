import numpy as np

arr1 = np.array([[1,2,3,4],[8,9,10,11]])
print (arr1)

# multiply array
print ("\bMultiplying array")
print (arr1 * arr1)


# subtraction array
print ("\bSubtraction array")
print (arr1 - arr1)


# division array
print ("\bDivision array")
print (1/arr1)  # reciprocal


# exponential array
print ("\bExponential array")
print (arr1**3)  # cube elements